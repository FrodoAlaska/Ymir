#pragma once

#include <string>

// Project type enum
///////////////////////////////////////////////////////////////////
enum project_type_e 
{
  PROJ_TYPE_LIB  = 0, 
  PROJ_TYPE_EXEC = 1
};
///////////////////////////////////////////////////////////////////

// Project desc struct
///////////////////////////////////////////////////////////////////
struct project_desc_t 
{
  std::string path, name;  
  std::string src_folder, header_folder, deps_folder; 
  bool has_scripts;
  int cpp_version;
  std::string compiler_flags, compiler_defines;
  project_type_e type;
};
///////////////////////////////////////////////////////////////////

// Project functions
///////////////////////////////////////////////////////////////////
void project_show_cli_help();
void project_create(project_desc_t& proj);
void project_create_cli(int argc, char** argv);
